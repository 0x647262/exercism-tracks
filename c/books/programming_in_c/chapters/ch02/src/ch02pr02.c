/*
 * Write a program the prints the following text
 *      1. In C, lowercase letters are significant.
 *      2. main() is where the program begins.
 *      3. Opening and closing brackets enclose the program statements in a
 * routine.
 *      4. All program statements are terminated by a semicolon.
 */

#include <stdio.h>

int main(void) {
  printf("1. In C, lowercase letters are significant.\n");
  printf("2. main() is where the program begins.\n");
  printf("3. Opening and closing brackets enclose the program statements in"
         "a routine.\n");
  printf("4. All program statements are terminated by a semicolon.\n");

  return 0;
}
